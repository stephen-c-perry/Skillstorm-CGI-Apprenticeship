public class Cat {
    public String name = "no name";
    public String hairColor = "no color";
    public Boolean spayedOrNeutered = false;
    public double weight = 0.0;
    public String owner = "no owner";

    public Cat(String name, String hairColor, Boolean spayedOrNeutered, double weight, String owner){
        this.name = name;
        this.hairColor = hairColor;
        this.spayedOrNeutered = spayedOrNeutered;
        this.weight = weight;
        this.owner = owner;
    }
}
